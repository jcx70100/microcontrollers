#include "reg52.h"  //定义51单片机特殊功能寄存器

/** 独立按键接口 */
sbit S7  = P3^0;
sbit S6  = P3^1;
sbit S5  = P3^2;
sbit S4  = P3^3;

//主函数
void main(void)
{    
    while(1)
    {
        if(S7 == 0)
        {
            P2 = ((P2&0x1f)|0x80);
			P0 = 0xff;  //关闭所有LED
			P2 &= 0x1f;
        }
        if(S6 == 0)
        {
            P2 = ((P2&0x1f)|0x80);
            P0 = 0x00;  //打开所有LED
			P2 &= 0x1f;
        }
        if(S5 == 0)
        {
            P2 = ((P2&0x1f)|0xa0);
			P0 &= ~(0x01<<6);  //Close 蜂鸣器
			P2 &= 0x1f;
        }
        if(S4 == 0)
        {
            P2 = ((P2&0x1f)|0xa0);
			P0 |= (0x01<<6); //Open蜂鸣器
			P2 &= 0x1f;
        }

    }
}